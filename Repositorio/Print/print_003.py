#Cadenas "f"

'''
A partir de la versión 3.6 de Python, se añadió (PEP 498), una nueva notación para cadenas llamada cadenas "f", 
que hace más sencillo introducir variables y expresiones en las cadenas. 
Una cadena "f" contiene variables y expresiones entre llaves "{}" que se sustituyen directamente por su valor. 
Las cadenas "f" se reconocen porque comienzan por una letra f antes de las comillas de apertura.
'''

nombre = "Alicia"
edad = 35
print(f"Me llamo {nombre} y tengo {edad} años.")